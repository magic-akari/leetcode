// Definition for a binary tree node.
export class TreeNode {
    /**
     * @param {number} val
     * @param {TreeNode | null} left
     * @param {TreeNode | null} right
     */
    constructor(val, left = null, right = null) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
